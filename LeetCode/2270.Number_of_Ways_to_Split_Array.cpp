class Solution {
public:
    int waysToSplitArray(vector<int>& nums) {
        ll ans=0;
        ll n=sz(nums);
        vector<ll> prefix(n,0), suffix(n,0);
        prefix[0]=nums[0];
        suffix[n-1]=nums[n-1];
        for(int i=1;i<n;i++){
            prefix[i]=prefix[i-1]+nums[i];
        }
        for(int i=n-2;i>=0;i--){
            suffix[i]=suffix[i+1]+nums[i];
        }
        for(int i=0;i+1<n;i++){
            if(prefix[i]>=suffix[i+1]){
                ans++;
            }
        }
        return (int)ans;
    }
};


