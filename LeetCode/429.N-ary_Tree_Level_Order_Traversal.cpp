class Solution {
public:
    vector<vector<int>> ans;
    queue<Node*> q;
    
    vector<vector<int>> levelOrder(Node* root) {
        if(root==nullptr){
            return ans;
        }
        q.push(root);
        while(sz(q)){
            queue<Node*>temp_q;
            vector<int> temp_ans;
            while(sz(q)){
                Node* node=q.front();
                temp_ans.pb(node->val);
                q.pop();
                for(auto x:node->children){
                    if(x!=nullptr){
                        temp_q.push(x);
                    }
                }
            }
            ans.pb(temp_ans);
            q=temp_q;
        }
        return ans;
    }
};



