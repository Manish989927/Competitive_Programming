class Solution {
public:
    Node* connect(Node* root) {
        if(root==nullptr){
            return nullptr;
        }
        queue<Node*> q;
        q.push(root);
        while(q.size()){
            queue<Node*>temp_q;
            while(q.size()){
                Node* node=q.front();
                q.pop();
                
                if(node->left!=nullptr){
                    temp_q.push(node->left);

                }
                if(node->right!=nullptr){
                    temp_q.push(node->right);
                }
                if(q.size()){
                    node->next=q.front();
                }
                else{
                    node->next=nullptr;
                }
            }
            q=temp_q;
        }
        return root;
    }
};



