class Solution {
public:
    int partitionDisjoint(vector<int>& nums) {
        int n=sz(nums);
        vl prefix(n,0),suffix(n,0);
        int mini=INT_MAX,maxo=0;
        for(ll i=0;i<n;i++){
            maxo=max(maxo,nums[i]);
            prefix[i]=maxo;
        }
        for(ll i=n-1;i>=0;i--){
            mini=min(mini,nums[i]);
            suffix[i]=mini;
        }
        for(ll i=0;i+1<n;i++){
            if(prefix[i]<=suffix[i+1]){
                return i+1;
            }
        }
        return -1;

    }
};



